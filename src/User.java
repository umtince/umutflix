
public class User {
	private String email,password,creditCardNumber,creditCardDate,creditCard3D;

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public String getCreditCardNumber() {
		return creditCardNumber;
	}

	public void setCreditCardNumber(String creditCardNumber) {
		this.creditCardNumber = creditCardNumber;
	}

	public String getCreditCardDate() {
		return creditCardDate;
	}

	public void setCreditCardDate(String creditCardDate) {
		this.creditCardDate = creditCardDate;
	}

	public String getCreditCard3D() {
		return creditCard3D;
	}

	public void setCreditCard3D(String creditCard3D) {
		this.creditCard3D = creditCard3D;
	}
}
